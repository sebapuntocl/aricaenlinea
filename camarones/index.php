                  <p>El pueblo pudo ser habitado por agricultores precolombinos, se encuentran numerosos muros de mampostería de piedra en la tradición de Guañacagua. Sirvió de etapa en la antigua senda norte – sur de Codpa a Putre.</p>
                </div>
