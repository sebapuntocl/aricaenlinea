                    <h3>Codpa </h3>
                  <p>Situadaa 114 Km. de Arica, a 1900 msnm. Se ubica en lo alto del río Vítor, se abastece de aguas puras y sus terrenos producen, paltas, guayabas, membrillos y uvas codpeñas, que crean el vino “pintatani”.</p>
