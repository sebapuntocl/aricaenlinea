                    <h3>La Iglesia de Chitita</h3>
                  <p>Ubicada en el extremo oriente del pueblo, comprimida entre la quebrada y el cerro. Reconstruida de una nave presenta muros de adobe, cubierta de barro sobre totora y esterilla, tijerales de madera rolliza. Portada de piedra adosada al muro testero, de factura sencilla inspirada en la de Belén.</p>
