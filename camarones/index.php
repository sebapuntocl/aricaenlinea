                    <h3>La Iglesia San Martín de Tours</h3>
                  <p>Importante centro misional durante la Colonia. Data de 1668, es una de las iglesias más antiguas de la zona de estilo colonial, construida en adobe con un altar repleto de imágenes de la época. </p>
                </div>
                <div role="tabpanel" class="tab-pane fade" id="vtab5">
                <img src="img/ARICA56.JPG" class="img-responsive" alt="Imagen responsive">
                    <h3>Ofragía</h3>
                  <p>A 5 kilómetros de Codpa, se observa un conjunto de petroglifos, de unos 60 bloques con grabados prehispánicos que datan de entre los años 1.000 a 1.500 D.C., aproximadamente. Fueron realizados por agricultores y ganaderos locales, que además realizaban viajes hacia otros valles y sectores mediante caravanas de llamas para intercambiar sus productos e ideas.</p>
                </div>
                <div role="tabpanel" class="tab-pane fade" id="vtab6">
                <img src="img/ARICA56.JPG" class="img-responsive" alt="Imagen responsive">
                    <h3>El pueblo de Guañacagua</h3>
                  <p>Ubicado a 4 Km. de Codpa en la quebrada de Sivitaya y de origen precolombino con una arquitectura típica de la zona costera, muros de adobe, cubiertas de barro sobre tijerales trapezoidales. Se conservan la escuela y la casa que le hace frente con un pilar de esquina de piedra. Pudo haber sido asentamiento inca por los restos de muros ejecutados en mampostería de piedra canteada, y por su ubicación estratégica en la unión de dos quebradas.</p>
                <p><b>Iglesia de Guañacagua:</b> Domina el pueblo desde la altura separada por la su construcción original, que probablemente era del siglo del siglo XVI, por su ábside ochavado, pero reconstruida probablemente a fines del Siglo XIX (inscripción de 1904 en la portada). Posee una nave sin capillas, retablo de piedra, muros de piedra canteada, cubierta de barro sobre totora y esterilla de paja brava, torre aislada, atrio cerrado y portada de piedra. El conjunto es de hermosas proporciones y representa la auténtica expresión artística local con influencias costinas y altiplánicas.</p>
                </div>
