                   <h3>Los atractivos del sector costero de la ciudad de Arica</h3>
                    <p> - <b>Las Playas del Litoral Sur:</b> Ubicadas a unos 15 a 20  minutos a pie, por el litoral costero, también se puede acceder por transporte público en la Avda. Máximo Lira.</p> 
                    <p>-<b>La Playa el Laucho:</b> Con escaso oleaje, ideal para el baño, las caminatas y baños de sol, cuenta con restaurante.</p>
                    <p>-<b>La Playa La Lisera:</b> Ubicada a 2,5 Km., en forma circular, no cuenta con oleaje, apta para el baño y buceo. Cuenta con equipamiento de: camarines, servicios, jardines,  expendio de bebidas y juegos infantiles.</p>
                    <p>-<b>La Playa Corazones:</b> A 15 Kms. al sur de Arica siguiendo el camino costero se encuentra esta playa de suave oleaje y mar calmo, es apta para baños de sol, picnic y pesca de orilla. Playa de arena fina y de color oscuro dominada por impresionantes acantilados. No existe transporte regular y no cuenta con suministro de agua potable. Hay instalaciones sanitarias y expendio de bebidas.</p> 
                    <p>-<b>El Sector de las Cuevas:</b> En dirección sur al final del sector costero, se encuentran las Cuevas de Anzota, Culave y Cutida. Es ideal poder realizar caminatas en donde se puede observar los vestigios de una antigua explotación de guano.</p>
                    <p>-<b>Las Playas del Litoral Norte:</b> Ubicada a unos 2 Kms. al norte de la ciudad, se puede acceder a ella en transporte público en la calle 18 de Septiembre o en colectivo en la calle Baquedano.</p>
                    <p>-<b>La Playa Chinchorro:</b> Extensa playa de escaso oleaje, apta para la natación, buceo y deportes aventuras como: el surf, bodyboard, landsailing</p>
                    <p>-<b>La Playa Las Machas:</b> situada al norte de la playa Chinchorro, no es apta para el baño producto de sus fuertes corrientes marinas. Se usa preferentemente para pesca y deportes como el surf.</p>
