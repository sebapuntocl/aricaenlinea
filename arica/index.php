                    <h3>Los atractivos del sector centro de la ciudad de Arica</h3>
                    <p>-<b> La Catedral de San Marcos (MN):</b> Construida entre 1871 y 1875 en los talleres de Gustavo Eiffel, en París. Fue armada en Arica en reemplazo de la iglesia matriz destruida por el terremoto de 1868. Obra inspirada en el estilo gótico. Su estructura es de fierro, sólo la puerta es de madera. Dos laterales y una de frente.</p>
                    <p>- <b>El Ferrocarril Arica-La Paz:</b> Construido en virtud del tratado de Paz Amistad y Comercio suscrito entre los gobiernos de Chile y Bolivia el 20 de Octubre de 1904. Fue inaugurado el 13 de Mayo de 1913. Tiene una longitud entre el puerto de Arica y La Paz de 457 Km.</p>
                    <p>- <b>El Edificio Ex-Aduana (MN):</b> Inaugurado en 1874. Esta obra fue prefabricada en los talleres de Gustavo Eiffel y armada en el lugar sobre cimientos de piedra. Cada ladrillo tiene estampado en su cara no visible el nombre del fabricante. Edificio que fue recientemente renovado en donde funciona la Casa de la Cultura de Arica, dependiente de la Ilustre Municipalidad de Arica, con exposiciones permanentes.</p>
                    <p>- <b>La Casa de la Gobernación:</b> Es la tercera construcción del ingeniero Gustavo Eiffel en Arica, que aguarda silenciosa su restauración para ser exhibida como un patrimonio cultural más de Arica. </p>
                    <p>- <b>El Morro de Arica (MN):</b> Peñón que geográficamente forma parte del cordón de cerros de la Cordillera de la Costa, está situado frente al mar y mide 130 m. de altura, ofreciendo una visión panorámica del mar, del puerto y de la ciudad. En la cima del Morro, se encuentra el Monumento al Soldado Desconocido y el Cristo de la Concordia.</p>
                    <p>- <b>La Península del Alacrán (MN):</b> Antiguamente separada por un canal de 460 m. de ancho, fue isla guanera hasta 1967, en ella se han encontrado restos de anzuelos y arpones de sus primeros habitantes, pescadores y recolectores de los siglos I y II. Posteriormente, fue fortificada por los españoles, artillada para repeler continuos ataques de corsarios y piratas como Sir Francis Drake, Bartholomew Sharp y John Watling, atraídos por los embarques de oro y plata que salían de este puerto hacia España, provenientes de Potosí. Después de 1964 fue unida a Arica. En la actualidad cobija el Club de Yates de Arica.</p>
                    <p>- <b>El Wateree (MN):</b> Las calderas de la embarcación Waterre permanecen en el mismo lugar dónde el último maremoto de 10877 dejó encallado al navío de bandera norteamericana. Sólo fierros oxidados recuerdan a la imponente figura de este barco de la Armada de EEUU. que había sido proyectado para la navegación fluvial. El maremoto de 1868 lo arrastró 3 Km. Tierra a adentro, donde posteriormente sirvió de hospital de emergencia. Luego, el segundo varó cerca de la playa, sirviendo de hotel para luego ser desmantelado. Ahora es Monumento Nacional. Sus fierros son carcomidos por la acción de la oxidación producto del húmedo ambiente marino. Lentamente desaparece un coloso que un día surcó los mares del sur.</p>
