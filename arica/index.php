                    <h3>Reseña histórica de la Provincia de Arica</h3>
                    <p>El 7 de Julio de 1565, el Virrey del Perú, don Lope García de Castro funda el Corregimiento de Arica con jurisdicción desde Tacna hasta el río Loa, y en 1570, Arica recibe el título de ciudad, otorgado por el rey don Felipe II de España, siendo la quinta ciudad más antigua de Chile. Así, Arica llega a ser el puerto más importante de la costa.</p>
                    <p>En 1611 Potosí era la ciudad más grande de occidente, con 160.000 habitantes y Arica era la gran productora de alfalfa para follaje.
                    Los continuos asaltos, la salida del mar, los terremotos ocurridos (1604, 1615, 1681), la decadencia del mineral de Potosí (1651 en adelante) y la terrible malaria, produjeron el despoblamiento de Arica. La población se instaló en áreas con mejores climas como Tacna, Belén y Putre, de modo que en 1713 quedaban solamente unas 150 familias. Con la creación del Virreinato de la Plata en 1776 (que incluía a Bolivia), el tráfico de Potosí se desvía a Buenos Aires. En 1782 se crea la Intendencia de Arequipa y la Administración abandona Arica.</p>
                   <p> Después de la independencia del Perú, la ciudad de Arica adquiere un nuevo impulso: se construye el ferrocarril a Tacna y más tarde las oficinas del gobierno, la aduana, la iglesia y el mercado para este activo puerto del sur peruano. El 7 de Junio de 1880, Arica es ocupada por el Ejército de Chile. En 1913 se inaugura el Ferrocarril de Arica a La Paz, transformándose nuevamente el puerto en el principal acceso a Bolivia. Su moderna estructura comenzó a gestarse en 1953, cuando el Presidente Carlos Ibáñez del Campo lo transformó en puerto libre para las actividades comerciales.</p>
                    <p>En 1958, el Presidente Carlos Ibáñez del Campo creó la Junta de Adelanto, caso único en Chile por la autonomía en sus decisiones. Esta Junta de Adelanto, impulsó la renovación urbana de Arica e incorporó las regiones altiplánicas. En 1964 se creó el Barrio Industrial, con armadurías de automóviles e industrias electrónicas destinadas al mercado latinoamericano, según las franquicias de la ALALC. La población creció junto con las nuevas obras, entre las que destacan el gran parque frente al Casino con maravillosas palmeras reales traídas de Brasil. El mejoramiento portuario y aeroportuario, el estadio, el equipamiento hotelero y la universidad, son ejemplos de esta etapa de desarrollo y modernización.</p>
                   <p> La política de libre mercado suprimió las franquicias de la industria local y el régimen de puerto libre. La fortaleza de hoy radica en el intenso tráfico comercial y en la instalación de poderosas industrias pesqueras con decenas de modernos barcos de pesca que repletan la bahía. Sus interesantes paseos, ferias y hoteles registran un intenso tráfico especialmente en verano cuando cientos de turistas vienen a gozar de sus playas.</p>
