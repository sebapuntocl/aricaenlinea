                    <p>Es la más septentrional de Chile, limita al norte con la República de Perú, al oeste con el Océano Pacífico, al sur con la Región de Tarapacá y al este con la República de Bolivia.</p>
                    <p>La creación de la Región de Arica y Parinacota, se formaliza con la prolongación de la ley Nº 20.175, por parte de la Presidenta Michelle Bachelet, el 23 de marzo del 2007. Posteriormente, el 11 de abril aparece publicado en el diario oficial y 180 días más tarde la Región de Arica y Parinacota empieza su funcionamiento.</p>
                    <p>Posee una superficie de 16.873,3 Km. que alberga una población estimada de 210.920 habitantes (según Censo 2012) y, administrativamente, se divide en dos provincias: Arica y Parinacota, que albergan un total de cuatro comunas.
                    La Provincia de Arica la conforman las comunas de Arica y Camarones, mientras que la Provincia de Parinacota cuenta con las comunas de Putre y General Lagos.</p>
