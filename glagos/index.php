                        <p>
                            La comuna de General Lagos pertenece a la Provincia de Parinacota, tiene una superficie de 2.364 km2, una población de 1.012 habitantes y una densidad de 0,42 habitantes/km2 (según Censo 1992). Limita al norte y al oeste con la República del Perú, al sur con las comunas de Arica y Putre y al este con la República de Bolivia. El tiempo promedio de acceso desde la ciudad de Arica es de 6 a 8 horas, vía ruta A-123 y CH-11 Arica- Tambo Quemado. Entre sus localidades más importantes tenemos, la capital comunal Visviri, Coronel Alcérreca, Humapalca, Villa Industrial, Tacora, Chislluma, Ancolacane, Colpitas, Putani, Guacollo, Cosapilla, Ancopujo, Chujluta y Nasahuento.
                        </p>
                        <p>
                            La temperatura media anual es de 4° C, con una fluctuación diaria de 25 grados C, con altas temperaturas en el día y bajas durante la noche. Las precipitaciones son abundantes durante el verano alcanzando un promedio anual de 306 mm. Su hidrografía es otro componente de interés tanto para la manutención de los ecosistemas como por su valor escénico en el paisaje en general, entre los que se pueden mencionar están: río Caquena y río Cosapilla. La vegetación la conforman especias típicas de la estepa andino son el principal recurso forrajero de la ganadería y fauna altiplánica, destacando la tola, paja brava, llareta, queñoa, bofedales y cactáceas. <br>
                             La fauna constituye otro atractivo del sector: vicuñas, zorros culpeo, ñandúes, pumas, guallatas, águilas, loros, gaviotas andinas y kiucha.
                        </p>
