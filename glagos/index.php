                        <h3>Visviri </h3>
                        <p>
                            Pueblo fronterizo, a 273 Km. de Arica (por Parinacota), capital de la comuna General Lagos. Se ubica a 3 Km. de la frontera con Bolivia, y a 12 Km. de la frontera con Perú. Los Miércoles y Sábados por las mañanas se realiza una “feria fantasma”, pudiendo cruzarse libremente a Bolivia. Los Domingos se realiza la feria “trifinio” en la frontera trinacional, llamada la “feria trifinio” donde se reúne la población aymará para el intercambio de productos. La fiesta más importante es la Cruz de Mayo, entre el 2 y el 5 de mayo.
                            <ul>
                                <li>
                                <p>
                                    <b>El volcán Tacora:</b> Es el primer gigante al entrar a Chile. Su esbelta estructura asciende hasta los 5.988 m.
                                </p>
                                </li>
                                <li>
                                <p>
                                    <b>Pukara de Visviri:</b> Poblado de altura o Pukará, levantado en la cima de un cerro isla de laderas abruptas, por efecto de la erosión de dos quebradas profundas que lo limitan por el norte y sur respectivamente. La ladera oeste es el más vulnerable, por esta razón se levantaron 5 corridas de muros defensivos de mampostería de piedra. Los tres primeros ubicados a media ladera son los más altos de más de un metro de altura. En el espacio entre el muro 3ro y 4to se recogió una bola tallada de basalto, posiblemente el proyectil de una onda, mientras que entre el muro 4to y 5to se registró una punta de proyectil de cuarzo, similares a las puntas encontradas en Las Cuevas de época Inka. Estos elementos podrían corresponder a artefactos utilizados en enfrentamientos bélicos ocurridos en el pukará. <br>
                                     Los recintos intra uros son de planta circular y servían para fines domésticos, de un diámetro promedio de 2,5 m y cubren un área total de aproximadamente 150 de largo por unos 50 m de ancho. Se contabilizaron más de 50 recintos dispersos, aunque se notan recintos pareados asociados a un patio común circundante. Los vanos de las puertas están orientados al este. <br>
                                     El lugar fue ocupado desde el período Intermedio Tardío (1100 – 1400 d.C.) hasta el período Inca (1400 – 1500 d.C.). En superficie se recuperó cerámica de los estilos San Miguel, Chilpe, Saxamar, Isluga Negro sobre Rojo. Funcionarios de la Municipalidad de General lagos, practicaron excavaciones ilegales en 1984, recuperando unos rollos laminados de metal bañados en oro. El sitio se encuentra en buen estado, con pircas de edificaciones abatidas parcialmente por movimientos telúricos y visitantes esporádicos. También hay señas de excavaciones ilegales. Está situado, al suroeste de la localidad de Visviri, colindante con la estancia Queñoanchinca, a 4240 m.s.n.m y distante 285 Km. de Arica. Se accede por la ruta A-123 (Parinacota – Visviri). Hacia el Pukará no existe camino de penetración, se debe caminar para acceder al lugar.
                                </p>
                                </li>
                                <li>
                                <p>
                                    <b>Feria Internacional Tripartito:</b> Feria internacional que se realiza en el límite fronterizo trinacional de Chile, Perú y Bolivia, todos los domingos del año. La cual se puede recorrer libremente, por los tres países en pocos minutos, siendo de gran importancia para la población local que trata de abastecerse o comercializar algunos productos típicos de la zona altiplánica o traídos de centros urbanos como Arica, Tacna o La Paz, como frutas y mercaderías. Al comenzar las actividades comerciales y de intercambio se realiza en los territorios de cada país el izamiento del pabellón patrio correspondiente a cada país y el canto de la respectiva canción nacional, demostrando en los chilenos asistentes un grado de patriotismo admirable. Se ubica a 14 Km. al Noroeste de Visviri se accede por ruta A-113 que une Visviri con el punto común de confluencia de los tres países.
                                </p>
                                </li>
                                <li>
                                <p>
                                    <b>El bofedal de Visviri:</b> Bofedal de tipo no salino, presenta una alta heterogeneidad, una diversidad de 14 especies vegetales y dentro de los animales predominantes se encuentran aves pequeñas, aves mayores y mamíferos entre los que destaca el ganado camélido propio de la zona altiplánica. Se ubica al sur de la localidad de Visviri, a 4.100 m.s.n.m y distante 285 kms de Arica. Se accede por la ruta A-123 (Parinacota – Visviri).
                                </p>
                                </li>
                            </ul>
