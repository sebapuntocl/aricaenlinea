                    <h3>El poblado de Cosapilla </h3>
                    <p>
                        <b>El poblado de Cosapilla:</b> Antiguo poblado de origen precolombino, en las cercanías del bofedal de Cosapilla que presenta en sus construcciones de adobe con techos de paja brava, un atractivo en forma conjunta con su iglesia y posas ceremoniales, asociados a la flora y fauna existente en el bofedal. Ubicado a 4.175 m.s.n.m. y distante a 268 Km. de Arica y 34 Km. de la capital comunal Visviri. Se accede por la ruta Ch-11(Arica – Tambo Quemado) y desvíos ruta A-123 y ruta A- 117 hacia Cosapilla
                        <ul>
                            <li>
                            <p>
                                <b>La Iglesia de Cosapilla:</b> Construida a diferencia de otras, en la misma cota del caserío, donde domina un gran espacio abierto delimitado por cuatro pozas ceremoniales en cada esquina y anexa al cementerio del lugar. Su construcción original es probablemente del siglo XVII. El edificio de una nave de piedra y barro, posee un retablo de piedra pintado a la cal, de factura primitiva pero valiosa, su atrio exterior está cerrado por un muro de piedra y barro encalado, con su torre incorporada al muro, destacando su doble arco de acceso al atrio.
                            </p>
                            <p>
                                El conjunto de la plaza con sus cuatro pozas ceremoniales, la iglesia y el cementerio constituyen una hermosa muestra de la arquitectura colonial que se estableció en este tipo de templos. Se ubica en el caserío de Cosapilla, a 4.175 m.s.n.m. y distante 268 Km. de Arica y 34 Km. de Visviri, se accede por ruta A-123 en el trayecto Parinacota – Visviri.
                            </p>
                            </li>
                            <li>
                            <p>
                                <b>La Iglesia de Pacollo:</b> Ubicada a corta distancia al norte de Cosapilla, no existe caserío organizado, la implementación de su iglesia puede atribuirse más bien a razones de algún simbolismo religioso del lugar. Su iglesia pequeña pero completa y bien proporcionada en su conjunto, con torre anexa y muro perforado encerrando el atrio de piedra y barro. Su Construcción original es probablemente del siglo XVII por la ubicación de su torre hacia el oriente
                            </p>
                            </li>
                            <li>
                            <p>
                                <b>El río Cosapilla:</b> Principal hoya hidrográfica de la comuna de General Lagos, cuyo escurrimiento es en dirección Sur – Norte internándose en territorio boliviano en el sector de Visviri. Destaca la formación de importantes bofedales como Cosapilla, Ancopujo, Guacollo, Nasahuento y Visviri, estos lugares presentan aglomeraciones de flora y fauna de fácil acceso para su observación, generalmente el ganado camélido se encuentra cerca de los diferentes caminos del área, y las diferentes especies de aves que habitan en los humedales. Su caudal estimado en 1,46 m3/seg. , recorriendo una superficie de 11.331 kms2. Recorre el sector oriental de la comuna a 4.030 m.s.n.m. Distante 260 Km. de Arica, se accede por la ruta A- 123 (Parinacota – Visviri).
                            </p>
                            </li>
                            <li><b>La cueva de Hakenasa:</b> Cueva de unos 10 x 15 m, formada en la base de un farellón de ignimbrita, en un ambiente de bofedal. Presenta una larga secuencia estratigráfica que incluye distintas épocas culturales que abarcan desde 8.000 años antes del presente hasta la época Incaica. El sitio es de gran relevancia para la arqueología regional, dada la cantidad de artefactos provenientes de excavaciones arqueológicas en el sitio. Ubicado a 4.250 m.s.n.m aproximadamente, junto a ruta Parinacota Visviri , al norte de la localidad de Pukara en la ladera norte de la desembocadura de la quebrada de Ancapujo en el río Cosapilla , Distante 260 Km. de Arica, se accede por la ruta A-123. </li>
                        </ul>
